#include "../CUB3D.h"

int	ft_count(char	*s1)
{
	int	count;
	int	i;

	i = 0;
	count = 0;
	while (s1[i])
	{
		if (s1[i] == ',')
			count++;
		i++;
	}
	return (count);
}
